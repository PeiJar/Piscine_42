void	alpha_mirror(char *str)
{
	for(int i = 0; str[i]; ++i)
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = 25 - (str[i] - 'a') + 'a';
		else if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = 25 - (str[i] - 'A') + 'A';
	}
}
