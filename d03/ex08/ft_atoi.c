int	ft_atoi(char *str)
{
	int	nb;
	int	neg;

	nb = 0;
	neg = 0;
	while (*str != '-' || (*str > '9' && *str < '0'))
		++str;
	if (*str == '-')
	{
		neg = 1;
		++str;
	}
	while (*str <= '9' && *str >= '0')
		nb = nb * 10 + *(str++) - '0';
	if (neg)
		nb = -nb;
	return (nb);
}
