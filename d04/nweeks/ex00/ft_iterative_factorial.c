int	ft_iterative_factorial(int nb)
{
	int i;
	int p;

	if (nb > 12 || nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	i = 1;
	p = 1;
	while (i <= nb)
		p *= i++;
	return (p);
}
