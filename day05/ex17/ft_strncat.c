char	*ft_strncat(char *dest, char *src, int nb)
{
	char *cpy;

	cpy = dest;
	while (*dest)
		++dest;
	while (*src && nb-- > 0)
		*(dest++) = *(src++);
	*dest = 0;
	return (cpy);
}
