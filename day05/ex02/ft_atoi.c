int	ft_atoi(char *str)
{
	int	nb;
	int	neg;

	nb = 0;
	neg = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n' || *str == '\v'
			|| *str == '\f' || *str == '\r')
		++str;
	if (*str == '-' || *str == '+')
	{
		neg = *str;
		++str;
	}
	while (*str <= '9' && *str >= '0')
		nb = nb * 10 + *(str++) - '0';
	if (neg == '-')
		nb = -nb;
	return (nb);
}
