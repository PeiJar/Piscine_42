char	*ft_strupcase(char *str)
{
	char *cpy;

	cpy = str;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
			*str = *str - 'a' + 'A';
		++str;
	}
	return (cpy);
}
