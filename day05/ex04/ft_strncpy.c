char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char	*cpy;

	cpy = dest;
	while (n != 0 && *src)
	{
		*(dest++) = *(src++);
		--n;
	}
	while (n != 0)
	{
		*(dest++) = 0;
		--n;
	}
	return (cpy);
}
