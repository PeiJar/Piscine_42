char	*ft_strlowcase(char *str)
{
	char	*cpy;

	cpy = str;
	while (*str)
	{
		if (*str >= 'A' && *str <= 'Z')
			*str = *str - 'A' + 'a';
		++str;
	}
	return (cpy);
}
