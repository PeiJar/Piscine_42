int	ft_sqrt(int nb)
{
	int odd;
	int summ;

	odd = 1;
	summ = 1;
	while (summ < nb && summ > 0)
	{
		odd += 2;
		summ += odd;
	}
	if (summ == nb)
		return (odd / 2 + 1);
	else
		return (0);
}
