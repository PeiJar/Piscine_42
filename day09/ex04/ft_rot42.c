char	*ft_rot42(char *str)
{
	char	*cpy;

	cpy = str;
	while (*str)
	{
		if (*str <= 'Z' && *str >= 'A')
			*str = (*str - 'A' + 16) % 26 + 'A';
		if (*str >= 'a' && *str <= 'z')
			*str = (*str - 'a' + 16) % 26 + 'a';
		++str;
	}
	return (cpy);
}
