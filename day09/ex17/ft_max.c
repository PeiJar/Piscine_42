int	ft_max(int *tab, int lenght)
{
	int	max;
	int	i;

	if (lenght <= 0)
		return (0);
	max = tab[0];
	i = 0;
	while (++i < lenght)
		max = (tab[i] > max) ? tab[i] : max;
	return (max);
}
